/// DC statistics bins per table.
pub const DC_STAT_BINS: usize = 64;
/// AC statistics bins per table.
pub const AC_STAT_BINS: usize = 256;

/// Extract Qe value from ARITAB entry.
#[inline]
pub fn qe_value(entry: u32) -> u32 {
    entry >> 16
}

/// Extract Next_Index_MPS from ARITAB entry.
#[inline]
pub fn next_mps(entry: u32) -> u8 {
    ((entry >> 8) & 0xFF) as u8
}

/// Extract Next_Index_LPS (including Switch bit) from ARITAB entry.
#[inline]
pub fn next_lps_with_switch(entry: u32) -> u8 {
    (entry & 0xFF) as u8
}

/// Extract Switch_MPS flag from LPS byte.
#[inline]
pub fn switch_mps(nl: u8) -> bool {
    (nl & 0x80) != 0
}

/// Extract Next_Index_LPS (without Switch bit).
#[inline]
pub fn next_lps_index(nl: u8) -> u8 {
    nl & 0x7F
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn aritab_has_114_entries() {
        assert_eq!(ARITAB.len(), 114);
    }

    #[test]
    fn aritab_entry_0_qe() {
        // Entry 0: Qe = 0x5a1d
        assert_eq!(qe_value(ARITAB[0]), 0x5a1d);
    }

    #[test]
    fn aritab_entry_113_fixed_probability() {
        // Entry 113: fixed probability 0.5, Qe = 0x5a1d
        assert_eq!(qe_value(ARITAB[113]), 0x5a1d);
        // Next state for both MPS and LPS is 113 (self-loop)
        assert_eq!(next_mps(ARITAB[113]), 113);
        assert_eq!(next_lps_index(next_lps_with_switch(ARITAB[113])), 113);
        assert!(!switch_mps(next_lps_with_switch(ARITAB[113])));
    }

    #[test]
    fn aritab_entry_0_transition() {
        // Entry 0: MPS -> state 1, LPS -> state 1 with switch
        assert_eq!(next_mps(ARITAB[0]), 1);
        let nl = next_lps_with_switch(ARITAB[0]);
        assert_eq!(next_lps_index(nl), 1);
        assert!(switch_mps(nl));
    }
}
